import { config as loadEnv } from 'dotenv';
import { chromium } from 'playwright';
import path from 'node:path';
loadEnv({ path: path.join(process.cwd(), '.env'), quiet: true });
loadEnv({ path: path.join(process.cwd(), '.env.local'), override: true, quiet: true });
const base='http://127.0.0.1:8084';
const email=String(process.env.E2E_ADMIN_EMAIL||'').trim();
const password=String(process.env.E2E_ADMIN_PASSWORD||'').trim();
const browser=await chromium.launch({headless:true});
const context=await browser.newContext({viewport:{width:430,height:920},serviceWorkers:'block'});
const page=await context.newPage();
page.on('response', async r=>{ const u=r.url(); if(u.includes('/auth/v1/user')||u.includes('/rest/v1/user_profile')){ let t=''; try{t=await r.text()}catch{} console.log('RES',r.status(),u,t.slice(0,240)); } });
await page.goto(`${base}/login?forceAuth=1`,{waitUntil:'domcontentloaded'});
await page.locator('input[type="email"]').first().fill(email);
await page.locator('input[type="password"]').first().fill(password);
const btns=page.getByText('Sign in',{exact:true}); await btns.nth((await btns.count())-1).click();
await page.waitForTimeout(5000);
await page.goto(`${base}/profile`,{waitUntil:'domcontentloaded'}); await page.waitForTimeout(2400);
await page.getByPlaceholder('Display name',{exact:true}).fill('Proof User');
await page.getByPlaceholder('City',{exact:true}).fill('Athens');
await page.getByPlaceholder('Your vibe',{exact:true}).fill('Proof vibe');
await page.getByText('Save profile',{exact:true}).first().click();
await page.waitForTimeout(7000);
const texts=await page.locator('text=/Profile|Could|Sign in|Session|missing|verify|saved|updated/i').allTextContents().catch(()=>[]);
console.log('TEXTS',JSON.stringify(texts.slice(0,30)));
await context.close();
await browser.close();
