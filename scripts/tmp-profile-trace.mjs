import { config as loadEnv } from 'dotenv';
import { chromium } from 'playwright';
import path from 'node:path';
loadEnv({ path: path.join(process.cwd(), '.env'), quiet: true });
loadEnv({ path: path.join(process.cwd(), '.env.local'), override: true, quiet: true });
const base=(process.env.BANDITS_WEB_BASE||'http://127.0.0.1:8084').replace(/\/$/,'');
const email=String(process.env.E2E_ADMIN_EMAIL||'').trim();
const password=String(process.env.E2E_ADMIN_PASSWORD||'').trim();
const browser=await chromium.launch({headless:true});
const context=await browser.newContext({ viewport:{width:430,height:920}, serviceWorkers:'block'});
const page=await context.newPage();
page.on('request', req=>{ const u=req.url(); if(u.includes('/rest/v1/user_profile')||u.includes('/auth/v1/user')) console.log('REQ',req.method(),u); });
page.on('response', async res=>{ const u=res.url(); if(!(u.includes('/rest/v1/user_profile')||u.includes('/auth/v1/user'))) return; let txt=''; try{txt=await res.text();}catch{} console.log('RES',res.status(),u,txt.slice(0,300)); });
await page.goto(`${base}/login?forceAuth=1`,{waitUntil:'domcontentloaded',timeout:120000});
await page.locator('input[type="email"]').first().fill(email);
await page.locator('input[type="password"]').first().fill(password);
const btns=page.getByText('Sign in',{exact:true});
await btns.nth((await btns.count())-1).click();
await page.waitForTimeout(5000);
await page.goto(`${base}/profile?trace=${Date.now()}`,{waitUntil:'domcontentloaded',timeout:120000});
await page.waitForTimeout(2500);
await page.getByPlaceholder('Display name',{exact:true}).fill('Trace User');
await page.getByPlaceholder('City',{exact:true}).fill('Trace City');
await page.getByPlaceholder('Your vibe',{exact:true}).fill('Trace vibe');
await page.getByText('Save profile',{exact:true}).first().click();
await page.waitForTimeout(7000);
const errTexts = await page.locator('text=/Sign in required|Invalid account|Session mismatch|Could not|missing|not available|verify|error/i').allTextContents().catch(()=>[]);
const dn=await page.getByPlaceholder('Display name',{exact:true}).inputValue().catch(()=> '');
const ci=await page.getByPlaceholder('City',{exact:true}).inputValue().catch(()=> '');
const vb=await page.getByPlaceholder('Your vibe',{exact:true}).inputValue().catch(()=> '');
console.log('FIELDS',JSON.stringify({dn,ci,vb}));
console.log('ERRS',JSON.stringify(errTexts));
await context.close();
await browser.close();
